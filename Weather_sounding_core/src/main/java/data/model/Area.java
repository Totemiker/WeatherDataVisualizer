package data.model;
/**
 * Data-Class<p>
 * Contains two Strings with the short Area-Code and the corresponding Area-Name
 * @author Tobias
 *
 */
public class Area 
{
	/**
	 * Contains the Area-Code
	 */
	private String areaCode;
	/**
	 * Contains the Area-Name
	 */
	private String areaName;
	/**
	 * 
	 * @param s1 Shorthand Area Code as String
	 * @param s2 Complete Area name as String
	 */
	public Area(String s1,String s2)
	{
		areaCode = s1;
		areaName = s2;
	}

	/**
	 * @return Getter for short Area Code
	 */
	public String getAreaCode() {
		return areaCode;
	}

	/**
	 * @return Getter for corresponding Area Name
	 */
	public String getAreaName() {
		return areaName;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((areaCode == null) ? 0 : areaCode.hashCode());
		result = prime * result + ((areaName == null) ? 0 : areaName.hashCode());
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Area other = (Area) obj;
		if (areaCode == null) {
			if (other.areaCode != null)
				return false;
		} else if (!areaCode.equals(other.areaCode))
			return false;
		if (areaName == null) {
			if (other.areaName != null)
				return false;
		} else if (!areaName.equals(other.areaName))
			return false;
		return true;
	}
	
}
